//Operator +

let namaDepan = "Albert";
let namaBelakang = "Einstein";

console.log(namaDepan + " " + namaBelakang); //output Albert Einstein

// operator +=

let Kata = "Hallo";
Kata += "Dunia";

console.log(Kata); //output Hallo Dunia

// Jika kita mnggabungkan tipe data string dan number maka hasil akhirny adalah string
let jumlah = "Kambing ada ";
jumlah += 5;

console.log(jumlah); //output kambing ada 5